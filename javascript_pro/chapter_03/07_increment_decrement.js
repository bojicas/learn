// Unary Operators
// Increment/Decrement

var age = 29;
++age;
console.log(age);

var age1 = 29;
--age1;
console.log(age1);

var age2 = 29;
var anotherAge = --age2 + 2;
console.log(age2);
console.log(anotherAge);

var num1 = 2;
var num2 = 20;
var num3 = --num1 + num2;
var num4 = num1 + num2;
console.log(num1);
console.log(num2);
console.log(num3);
console.log(num4);

var num01 = 2;
var num02 = 20;
var num03 = num01-- + num02;
var num04 = num01 + num02;
console.log(num01);
console.log(num02);
console.log(num03);
console.log(num04);
