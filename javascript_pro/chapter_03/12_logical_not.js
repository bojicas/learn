// Boolean Operators
// Logical NOT

console.log(!false);
console.log(!"blue");
console.log(!0);
console.log(!NaN);
console.log(!"");
console.log(!12345);

// convert a value into its Boolean equivalent
console.log(!!"blue");
console.log(!!0);
console.log(!!NaN);
console.log(!!"");
console.log(!!12345);
